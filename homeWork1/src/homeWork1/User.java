package homeWork1;

public class User {
	
	public User(int id,String userName,String firstName,String lastName,String email,String password) {
		
		this();
		this.id=id;
		this.userName=userName;
		this.firstName=firstName;
		this.lastName=lastName;
		this.email=email;
		this.password=password;
	}
	
	public User() {}
	
	
	
	int id;
	String userName;
	String firstName;
	String lastName;
	String email;
	String password;
	

}
